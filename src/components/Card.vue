/* eslint-disable global-require */
<template>
      <transition name="flip">
          <div
          v-bind:class="flipped ? 'flip-container flipped': 'flip-container'">
              <div v-if="show" class="flipper">
                  <button
                  v-bind:id="id"
                  class="front"
                  v-on:click="toggle()">
                  </button>
                  <button class="back">
                      <div>
                          <img v-bind:src="require('../assets/' + back)" />
                      </div>
                  </button>
              </div>
          </div>
      </transition>
</template>

<script>
export default {
  props: {
    id: String,
    back: String,
    show: Boolean,
  },
  data() {
    return {
      flipped: false,
    };
  },
  methods: {
    toggle() {
      // eslint-disable-next-line no-param-reassign
      this.flipped = !this.flipped;
      setTimeout(() => {
        this.flipped = false;
      }, 2 * 1000);
    },
  },
};
</script>

